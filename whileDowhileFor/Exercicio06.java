package whileDowhileFor;

public class Exercicio06 {
	
	public static void main(String[] args) {
		
		//https://www.slideshare.net/loianeg/curso-java-basico-exercicios-aulas-16-17
		
		for(int i=0;i<=20;i++) {
			System.out.println(i);
		}
		int i=0;
		
		while(i<=20) {
			System.out.print(i);
			i++;
		}
	}

}
